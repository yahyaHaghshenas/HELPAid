        final String username = jTextFieldUsername.getText();        
        final String password = jPasswordField.getText();
        User u = HelpAidBase.helpAid.findUser(username);
        if (u != null) {
            if (u.getPassword().equals(password)) {
                loginUser = helpAid.findUser(username);
                if(password.equals("admin")){
                    this.jPanelLanding.setVisible(false);
                    this.jPanelAdminMenu.setVisible(true);
                }
                else if( u instanceof base.Applicant){
                    this.jPanelLanding.setVisible(false);
                    this.jPanelApplicantMenu.setVisible(true);
                } else if (u instanceof base.OrganizationRep){
                    this.jPanelLanding.setVisible(false);
                    this.jPanelRepMenu.setVisible(true);
                }
                
            }
        }
