    private void jButtonRecDisbursementActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRecDisbursementActionPerformed
        final String aplID = jTextFieldApplicant.getText() != null ? jTextFieldApplicant.getText() : "";
        Applicant apl  = HelpAidBase.helpAid.findApplicantID(aplID);
        
        
        final String apID = jTextField5.getText() != null ? jTextField5.getText() : "";
        Appeal ap  = HelpAidBase.helpAid.findAppeal(apID);
        if (ap == null) {
            JOptionPane.showMessageDialog(null, ap + " Appeal did not find", "No Such Appeal", JOptionPane.ERROR_MESSAGE);
        } else {
            final String disbDateStr = jTextFieldDisbDate.getText() != null ? jTextFieldDisbDate.getText() : "";
            LocalDate disbDate = LocalDate.parse(disbDateStr, formatter);
            final String cashStr = jTextFieldCash.getText() != null ? jTextFieldCash.getText() : "";
            double cash = 0.0;
            try {
                cash = Double.parseDouble(cashStr);
            } catch(Exception _){};
            final String valueStr = jTextFieldGoods.getText() != null ? jTextFieldGoods.getText() : "";
            double value = 0.0;
            try {
                value = Double.parseDouble(valueStr);
            } catch(Exception _){};
            
            String message = null;
            if (aplID.isEmpty()) {
                message = "aplicant ID is required, ";
            } else if (apID.isEmpty()) {
                message = "Appeal ID is required, ";
            } else if (disbDateStr.isEmpty()) {
                 message = "Date is required, ";
            }
            if (message != null) {
                JOptionPane.showMessageDialog(null, message, "Add Applicant", JOptionPane.ERROR_MESSAGE);
            }
            Disbursement ds = new Disbursement(disbDate, cash, value, apl);
            HelpAidBase.addDisburs(apl, ds, ap);
            JOptionPane.showMessageDialog(null, ds + " Successfully Added...", "Add Disbursement", JOptionPane.INFORMATION_MESSAGE);
            
            // toggle view
            this.jPanelAddDisbursement.setVisible(false);
            this.jPanelRepMenu.setVisible(true);
        }
    }//GEN-LAST:event_jButtonRecDisbursementActionPerformed

