    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        final String orgId = jComboBoxOrg.getSelectedItem().toString();
        Organization org = HelpAidBase.helpAid.getOrganizations().stream()
                .filter(o -> o.getOrgID().equals(orgId))
                .findFirst()
                .orElse(null);
        
        final String name = jTextField16.getText() != null ? jTextField16.getText() : "";
        final String email = jTextField20.getText() != null ? jTextField20.getText() : "";
        final String phone = jTextField18.getText() != null ? jTextField18.getText() : "";
        final String id = jTextField17.getText() != null ? jTextField17.getText() : "";
        final String address = jTextField19.getText() != null ? jTextField19.getText() : "";
        final String incomeStr = jTextField21.getText() != null ? jTextField21.getText() : "";
        double income = 0.0;
        try {
            income = Double.parseDouble(incomeStr);
        } catch(Exception _){};
        
        // validation
        String message = null;
        if (name.isEmpty()) {
            message = "name is required, ";
        } else if (email.isEmpty()) {
            message = "email is required, ";
        } else if (phone.isEmpty()) {
            message = "phone is required, ";
        } else if (id.isEmpty()) {
            message = "id is required, ";
        } else if (address.isEmpty()) {
            message = "address is required, ";
        } else if (income == 0.0) {
            message = "income is required.";
        }
        if (message != null) {
            JOptionPane.showMessageDialog(null, message, "Add Applicant", JOptionPane.ERROR_MESSAGE);
        } else {
            Applicant appl = new Applicant(name, email, phone, id, address, income, org);
            final String description = jTextFieldDecDescription.getText() != null ? jTextFieldDecDescription.getText() : "";
            Document doc = new Document(selectedFileName, description);
            
            // add & save to file
            HelpAidBase.addApplicant(appl, doc, org);
            JOptionPane.showMessageDialog(null, name + " Successfully Added...", "Add Applicant", JOptionPane.INFORMATION_MESSAGE);
            
            // TOGGLE VIEW
            this.jPanelAddApplicant.setVisible(false);
            this.jPanelRepMenu.setVisible(true);
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

