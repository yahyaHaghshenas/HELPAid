    private void jButtonAddAppealActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddAppealActionPerformed
        final String fromDateStr = jTextFieldAppealFromDate.getText() != null ? jTextFieldAppealFromDate.getText() : "";
        LocalDate fromDate = LocalDate.parse(fromDateStr, formatter);
        final String toDateStr =  jTextFieldAppealToDate.getText() != null ?  jTextFieldAppealToDate.getText() : "";
        LocalDate toDate = LocalDate.parse(toDateStr, formatter);

        final String orgId = jComboBox2.getSelectedItem().toString();
             Organization org = HelpAidBase.helpAid.getOrganizations().stream()
            .filter(o -> o.getOrgID().equals(orgId))
            .findFirst()
            .orElse(null);
        final String desc = jTextArea2.getText() != null ? jTextArea2.getText() : "";
        
        String message = null;
            if (fromDateStr.isEmpty()) {
                message = "From Date ID is required, ";
            } else if (toDateStr.isEmpty()) {
                message = "To Date is required, ";
            }
            if (message != null) {
                JOptionPane.showMessageDialog(null, message, "Add Applicant", JOptionPane.ERROR_MESSAGE);
            }
        Appeal ap = new Appeal(fromDate, toDate, desc,  org);
        HelpAidBase.addAppeal(ap, org);
        JOptionPane.showMessageDialog(null, ap + " Successfully Added...", "Add Apeal", JOptionPane.INFORMATION_MESSAGE);

        // toggle view
        this.jPanelAddAppeal.setVisible(false);
        this.jPanelLanding.setVisible(true);
    }//GEN-LAST:event_jButtonAddAppealActionPerformed

